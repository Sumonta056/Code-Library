string minLexRotation(string str)
{
    int n = str.length();
    string arr[n];
    string concat = str + str;

    for (int i = 0; i < n; i++)
        arr[i] = concat.substr(i, n);

    sort(arr, arr + n);

    return arr[0];
}

string maxLexRotation(string str)
{
    string inp = str;
    string ans = inp;
    int cnt = 1;

    for (int i = 0; i < inp.length(); i++)
    {
        if (!i)
            continue;
        ans = max(ans, inp.substr(i) + inp.substr(0, cnt));
        cnt++;
    }

    return ans;
}


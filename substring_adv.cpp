ll mod_mul(ll a, ll b)
{
    a = a % mod;
    b = b % mod;
    return (((a * b) % mod) + mod) % mod;
}
ll mod_add(ll a, ll b)
{
    a = a % mod;
    b = b % mod;
    return (((a + b) % mod) + mod) % mod;
}

bool isPalindrome(string S)
{
    string P = S;
    reverse(P.begin(), P.end());

    if (S == P)
        return 1;
    else
        return 0;
}


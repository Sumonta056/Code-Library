const ll node = 101;

ll weight_track[node][node];
ll weight = 0;

vll visited(node, 0);
vll adj[node];

void dfs(ll cNode, ll previousNode)
{
    if (visited[cNode])
        return;

    visited[cNode] = 1;

    ll child = adj[cNode][0] == previousNode ? adj[cNode][1] : adj[cNode][0]; 

    // * every node is connected with two node in a cycle
    // * if current node = previous node then shift it to left or right

    weight += weight_track[cNode][child];

    dfs(child, cNode);
}

int balancedBTS ( int h)
{
    if(h == 0 || h == 1) return 1 ;

    // * multipication will have huge value : so we will modulo 
    int m = pow(10 ,9) +7 ;

    int x = balancedBTS(h-1);
    int y = balancedBTS(h-2);

    // ? formula is ans = (x*x) + (2*x*y);

    long res1 = (long)x*x;
    long res2 = (long)x*y*2;

    int ans1 = (int)(res1%m);
    int ans2 = (int)(res2%m);

    int ans = (ans1 + ans2) % m ;

    return ans ;
    
}


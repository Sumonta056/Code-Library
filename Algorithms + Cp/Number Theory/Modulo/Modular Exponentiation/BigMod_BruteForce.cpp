ll bigMod(ll value, ll power, ll mod)
{
    ll ans = 1 % mod;
    value = value % mod;

    for (int i = 1; i <= power; i++)
    {
        ans = (ans * value) % mod;
    }

    return ans;
}

// * Linear Solution O(power)

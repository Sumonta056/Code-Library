bool pairCompare(const pair<int, int> &i, const pair<int, int> &j)
{
    return i.first < j.first;
}

int main()
{

    int n;
    cin >> n;

    vector<pair<int, int>> arr(n);
    set<int> s;

    for (int i = 0; i < n; i++)
    {
        cin >> arr[i].first;
        s.insert(arr[i].first);
        arr[i].second = i;
    }

    if (s.size() == n)
        cout << -1 << endl;
    else
    {
        int minimum = mod;

        sort(arr.begin(), arr.end(), pairCompare);
        
        for (int i = 1; i < n; i++)
        {
            if (arr[i].first == arr[i - 1].first)
                minimum = min(minimum, abs(arr[i].second - arr[i - 1].second));
        }

        cout << minimum << endl;
    }
}

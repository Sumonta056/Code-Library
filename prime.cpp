ll check_prime(ll numm)
{
    ll ff = 1;
    if (numm == 1)
        return 0;

    for (ll ii = 2; ii * ii <= numm; ii++)
    {
        if (numm % ii == 0)
        {
            ff = 0;
            break;
        }
    }
    // cout << ff <<endl;
    return ff;


}

